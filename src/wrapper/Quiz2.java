package wrapper;

// 정수 10, 실수 1.123, 문자 c, boolean true 문자열에 저장하기
public class Quiz2 {

	public static void main(String[] args) {
		
		
		//toString: 데이터를 String으로 반환하는함수
		//10 -> "10"
		String str1 = Integer.toString(10);
		// 1.123 -> "1.123"
		String str2 = Double.toString(1.123);
		// 'c' -> "c"
		String str3 = Character.toString('c');
		// true -> "true"
		String str4 = Boolean.toString(true);


	}

}
